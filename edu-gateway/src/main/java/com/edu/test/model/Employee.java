package com.edu.test.model;

public class Employee {
    private String id;
    private String intranetId;
    private String employee_systemrole;
    private String employee_identification;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIntranetId() {
        return intranetId;
    }

    public void setIntranetId(String intranetId) {
        this.intranetId = intranetId;
    }

    public String getEmployee_systemrole() {
        return employee_systemrole;
    }

    public void setEmployee_systemrole(String employee_systemrole) {
        this.employee_systemrole = employee_systemrole;
    }

    public String getEmployee_identification() {
        return employee_identification;
    }

    public void setEmployee_identification(String employee_identification) {
        this.employee_identification = employee_identification;
    }

}
